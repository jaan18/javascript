const numero1 = 2;
const numero2 = 3;

function sumar(a, b) {
  return a + b;
}

function restar(a, b) {
  return a - b;
}

function multiplicar(a, b) {
  return a * b;
}

function dividir(a, b) {
  return a / b;
}


console.log("Suma", sumar(numero1, numero2));
console.log("Resta", restar(numero1, numero2));
console.log("Multiplicacion", multiplicar(numero1, numero2));
console.log("Division", dividir(numero1, numero2));
